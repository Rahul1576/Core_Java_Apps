package Practice;
import java.util.*;
public class Print_n_2_0 {

	public static void main(String[] args) {
		Scanner s=new Scanner(System.in);
		System.out.println("Enter a number");
		int n=s.nextInt();
		System.out.println("Number are :");
		for(int i=n;i>0;i--)
		{
			System.out.print(" "+i);
		}

	}

}
