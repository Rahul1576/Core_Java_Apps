import java.util.*;
public class Printto_n {

	public static void main(String[] args) {
		Scanner s=new Scanner(System.in);
		System.out.println("Enter a number");
		int n=s.nextInt();
		System.out.println("Number are :");
		for(int i=0;i<n;i++)
		{
			System.out.print(" "+i);
		}

	}

}
