package maccess;

public class DemoWrapperclass4 {

	public static void main(String[] args) {
	
	//	Auto Boxing;
		Integer ob1=12;
		Float ob2=12.35F;
		Character ob3 ='A';
		Boolean ob4=true;
		
		//Unboxing process;
		
		int i=ob1;
		float f=ob2;
		char ch=ob3;
		boolean b=ob4;
		
		
		System.out.println("display values 2");
		System.out.println("int values :"+i);
		System.out.println("float values:"+f);
		System.out.println("char values :"+ch);
		System.out.println("boolean values :"+b);
		

	}

}
